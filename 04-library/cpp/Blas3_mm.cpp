// src/MatrixMatrixOps.cpp
#include "SimpleBLAS.h"

namespace SimpleBLAS {

void matrixMatrixMultiply(const double* A, const double* B, double* C, int rowsA, int colsA, int colsB) {
    for (int i = 0; i < rowsA; ++i) {
        for (int j = 0; j < colsB; ++j) {
            double sum = 0.0;
            for (int k = 0; k < colsA; ++k) {
                sum += A[i * colsA + k] * B[k * colsB + j];
            }
            C[i * colsB + j] = sum;
        }
    }
}

} // namespace SimpleBLAS
